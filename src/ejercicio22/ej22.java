package ejercicio22;

public class ej22 {

	public static void main(String[] args) {
		System.out.println("Números del 1 al 5:");
		for (int i = 1; i <= 5; i++) {
			System.out.print(i + " ");
		}
		System.out.println();

		System.out.println("Números en orden inverso:");
		for (int i = 5; i >= 1; i--) {
			System.out.print(i + " ");
		}
	}

}
